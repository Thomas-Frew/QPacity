/// <summary>
/// A custom variable type to organise the properties of QPacity code.
/// </summary>
public enum QProp
{
    /// <summary>
    /// The QPacity code's header.
    /// </summary>
    Header = 0,

    /// <summary>
    /// The QPacity code's owner's name.
    /// </summary>
    Name = 1,

    /// <summary>
    /// The QPacity code's owner's phone number.
    /// </summary>
    PhoneNumber = 2,

    /// <summary>
    /// The QPacity code's check digits.
    /// </summary>
    CheckDigits = 3,
}