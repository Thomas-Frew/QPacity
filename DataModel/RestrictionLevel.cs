/// <summary>
/// A custom variable type for South Australia's COVID restriction levels.
/// </summary>
public enum RestrictionLevel
{
    /// <summary>
    /// Level zero, no restrictions.
    /// </summary>
    Level_0 = 0,

    /// <summary>
    /// Level one, watch and assess restrictions.
    /// </summary>
    Level_1 = 1,

    /// <summary>
    /// Level two, low restrictions.
    /// </summary>
    Level_2 = 2,

    /// <summary>
    /// Level three, medium restrictions.
    /// </summary>
    Level_3 = 3,

    /// <summary>
    /// Level four, high resitrctions.
    /// </summary>
    Level_4 = 4,

    /// <summary>
    /// Level five, stay at home.
    /// </summary>
    Level_5 = 5
}