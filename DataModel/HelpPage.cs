/// <summary>
/// A custom variable type for QPacity's help pop up pages.
/// </summary>
public enum HelpPage
{
    /// <summary>
    /// The introduction page.
    /// </summary>
    Introduction = 0,

    /// <summary>
    /// The setup page, for room dimensions.
    /// </summary>
    RoomDimensions = 1,

    /// <summary>
    /// The setup page, for other settings.
    /// </summary>
    Settings = 2,

    /// <summary>
    /// The interface explanation page.
    /// </summary>
    Interface = 3,

    /// <summary>
    /// The scanner explanation page.
    /// </summary>
    Scanner = 4,

    /// <summary>
    /// The code generator/issuer explanation page.
    /// </summary>
    CodeGenerator = 5,

    /// <summary>
    /// The current guests explanation page.
    /// </summary>
    CurrentGuests = 6,

    /// <summary>
    /// The conclusion page.
    /// </summary>
    Conclusion = 7
}