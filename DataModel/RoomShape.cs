/// <summary>
/// A custom variable type for the possible shapes of a room.
/// </summary>
public enum RoomShape
{
    /// <summary>
    /// A rectangular room.
    /// </summary>
    Rectangular = 0,

    /// <summary>
    /// A circular room.
    /// </summary>
    Circular = 1,

    /// <summary>
    /// A custom room size.
    /// </summary>
    Custom = 2
}