/// <summary>
/// A custom variable type for the possible shapes of a room.
/// </summary>
public enum SeatingPlan
{
    /// <summary>
    /// An seated event.
    /// </summary>
    Seated = 0,

    /// <summary>
    /// A standing event.
    /// </summary>
    Standing = 1,

    /// <summary>
    /// An indoor sporting event.
    /// </summary>
    Sports = 2
}