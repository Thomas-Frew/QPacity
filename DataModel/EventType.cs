/// <summary>
/// A custom variable type for the possible types of events in South Australia, each with their own restrictions.
/// </summary>
public enum EventType
{
    /// <summary>
    /// An at-home event.
    /// </summary>
    At_Home = 0,

    /// <summary>
    /// A private event.
    /// </summary>
    Private = 1,

    /// <summary>
    /// A public event.
    /// </summary>
    Public = 2
}